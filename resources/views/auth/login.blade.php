        var ConsentInspector = (function() {

            var REQUIRED_COOKIES = ['consent', '_optisure_session'];

            function getConsentFromCookie() {
                this.CONSENT_COOKIE = 'consent';
                const existingConsent = document.cookie
                    .split('; ')
                    .find(row => row.startsWith(`${this.CONSENT_COOKIE}=`));
                if (existingConsent) {
                    try {
                        return JSON.parse(existingConsent.split('=')[1]);
                    } catch {
                        console.error("Failed to parse consent")
                    }
                }
            }

            function cleanUpCookies(cookiePrefixes) {
                var anyUTCDateInPast = 'Thu, 01 Jan 1970 00:00:00 UTC';
                cookiePrefixes.forEach((prefix) => {
                    filteredCookieNames(prefix).forEach((name) => {
                        document.cookie = name + "=; expires=" + anyUTCDateInPast +
                            "; path=/; domain=." + getDomain();
                        document.cookie = name + "=; expires=" + anyUTCDateInPast +
                            "; path=/; domain=." + getDomainWithSubdomains();
                    })
                })
            }

            function cookieNamesStartsWith(string) {
                return document.cookie.split(';').
                map(c => c.trim()).
                filter(c => c.startsWith(string)).
                map(c => c.split('=')[0]);
            }

            function filteredCookieNames(string) {
                return cookieNamesStartsWith(string).filter(c => !REQUIRED_COOKIES.includes(c.split('=')[0]))
            }

            function getDomain() {
                return getDomainWithSubdomains().split('.').slice(-2).join('.');
            }

            function getDomainWithSubdomains() {
                return location.hostname;
            }

            function cleanUpStorages(storageItemsPrefixes) {
                storageItemsPrefixes.forEach((prefix) => {
                    localStorageItemsStartsWith(prefix).forEach(storageItem => {
                        localStorage.removeItem(storageItem);
                    })
                    sessionStorageItemsStartsWith(prefix).forEach(storageItem => {
                        sessionStorage.removeItem(storageItem);
                    })
                })
            }

            function localStorageItemsStartsWith(prefix) {
                var storageItems = [];
                for (var i = 0; i < localStorage.length; i++) {
                    var item = localStorage.key(i)
                    if (item.startsWith(prefix)) {
                        storageItems[i] = item;
                    }
                }
                return storageItems;
            }

            function sessionStorageItemsStartsWith(prefix) {
                var storageItems = [];
                for (var i = 0; i < sessionStorage.length; i++) {
                    var item = sessionStorage.key(i)
                    if (item.startsWith(prefix)) {
                        storageItems[i] = item;
                    }
                }
                return storageItems;
            }

            function isEligible(categoryIdent, consent) {
                return versionValid(consent.version) &&
                    categoryExist(categoryIdent, consent) &&
                    categoryEnabled(categoryIdent, consent)
            }

            function categoryExist(categoryIdent, consent) {
                return consent.categories.some(category => category.identifier === categoryIdent);
            }

            function categoryEnabled(categoryIdent, consent) {
                return consent.categories.find(category => category.identifier === categoryIdent)?.enabled;
            }

            function versionValid(consentVersion) {
                return 1 === consentVersion
